package com.example.dangdiary.diet.java;

public class FoodInfo {


    private long id;
    private String name;
    private int amount;
    private int calorie;
    private int carbohydrate;
    private int protein;
    private int fat;
    private int sugars;
    private int sodium;




    public long getId() {
        return id;
    }

    public String getName(){
        return name;
    }

    public int getCalorie() {
        return calorie;
    }

    public int getCarbohydrate() {
        return carbohydrate;
    }

    public int getProtein() {
        return protein;
    }

    public int getFat() {
        return fat;
    }

    public int getSugars() {
        return sugars;
    }

    public int getSodium() {
        return sodium;
    }
}
